public class Main {
    public static void main(String[] args) {
        int a = 5;
        int b = 10;

        //Addition
        int sum = a + b;
        System.out.println(sum);
        //Difference
        int diff = a - b;
        System.out.println(diff);
        //Multiplication
        int multi = a * b;
        System.out.println(multi);
        //Division
        int div = a / b;
        System.out.println(div);
    }
}